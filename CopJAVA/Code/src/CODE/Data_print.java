package CODE;

public class Data_print {

	public static void main(String[] args) {
		int i = 12;
		char ch = 'A';
		float f = 12.12f;
		double d = 12.1212;
		System.out.println("i = " + i);
		System.out.println("ch = " + ch);
		System.out.println("f = " + f);
		System.out.println("d = " + d);

	}

}
